let fruits = ['apples', 'oranges', 'bananas', 'cherries']; //This is the same situation as with the for loop file
let numberOfFruits = fruits.length;

let i = 0;
while (i < numberOfFruits) { //This is the basis of a while loop
  console.log(fruits[i] + ' are delicious!'); //this will continue to execute while i is less than numberOfFruits
  i++; //incrementors are perfectly valid in while loops, and are required so that the while loop doesn't continue forever
}

console.log('I love fruit!');



