function turnPink() {
    let pink = document.getElementById("box"); //finds the div with id of box
    pink.style.backgroundColor = "lightpink"; //turns the div with id of box a pink color when the mouse enters
}

function turnYellow() {
    let yellow = document.getElementById("box"); //finds the div with id of box
    yellow.style.backgroundColor = "yellow"; //turns the div with id of box a yellow color when the mouse leaves
}